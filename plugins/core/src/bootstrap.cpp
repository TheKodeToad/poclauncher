#include <QPluginLoader>

#include "CorePlugin.h"

static CorePlugin* findCorePlugin()
{
    for (const QStaticPlugin& plugin : QPluginLoader::staticPlugins()) {
        auto core = qobject_cast<CorePlugin*>(plugin.instance());

        if (core != nullptr)
            return core;
    }

    throw std::runtime_error("No CorePlugin instance present");
}

void bootstrap()
{
    CorePlugin* core = findCorePlugin();
    core->bootstrap();
}
